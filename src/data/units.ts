/** Sustituye MAR por OCEANO en la vista para unificar nomenclatura. */
function vistaMarToOceano(v: string): string {
  return v
    .replace(/\bMAR\b/gi, "OCEANO")
    .replace(/,\s*MAR\s+/gi, ", OCEANO ")
    .trim();
}

/**
 * Vista efectiva (siempre en mayúsculas; MAR unificado a OCEANO).
 * Sunrise: usa unit.vista; desde planta 6 se ve océano, si viene GOLF o vacío se considera OCEANO Y GOLF.
 * Sunset: planta 1–5 siempre GOLF Y PISCINA; planta 6+ solo 611–616, 711–716, 811–816 tienen GOLF, PISCINA Y OCEANO.
 */
export function getEffectiveVista(unit: Unit): string {
  let out: string;
  if (unit.fase === "Sunrise") {
    const raw = (unit.vista ?? "").trim() || "";
    if (unit.nivel >= 6 && (!raw || raw.toUpperCase() === "GOLF")) {
      out = "OCEANO Y GOLF";
    } else {
      out = vistaMarToOceano((raw || "GOLF").toUpperCase());
    }
  } else if (unit.fase === "Sunset") {
    if (unit.nivel <= 5) {
      out = "GOLF Y PISCINA";
    } else {
      const oceanRanges: [number, number][] = [
        [611, 616],
        [711, 716],
        [811, 816],
      ];
      const hasOcean = oceanRanges.some(
        ([min, max]) => unit.unidad >= min && unit.unidad <= max
      );
      out = hasOcean ? "GOLF, PISCINA Y OCEANO" : "GOLF Y PISCINA";
    }
  } else {
    const raw = (unit.vista ?? "").trim() || "";
    out = vistaMarToOceano(raw.toUpperCase()) || "GOLF";
  }
  return out.trim();
}

/** True si la vista efectiva incluye océano (para mostrar emoji 🌊). Oceano y MAR se tratan igual. */
export function vistaIncluyeOceano(vista: string): boolean {
  const v = vista.toUpperCase();
  return v.includes("OCEANO") || v.includes("MAR") || v.includes("PLAYA");
}

